#include <stdio.h>

int main() {
    char str[100];
    int i = 0;

    printf("Enter a string (including spaces): ");
    fgets(str, sizeof(str), stdin);

    while (str[i] != '\0') {
        if (str[i] == ' ') {
            str[i] = '-';
        }
        i++;
    }

    printf("\nModified string: %s", str);

    return 0;
}